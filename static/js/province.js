// loader function
window.onload = function () {
  const provinceSelection = document.querySelector("#Province"),
      citySelection = document.querySelector("#City"),
      zipcodeSelection = document.querySelector("#Zipcode");

  // console.log(zipcodeSelection)
  citySelection.disabled = true;
  zipcodeSelection.disabled = true;

  for (let province in provinceInfo) {
    provinceSelection.options[provinceSelection.options.length] = new Option(
        province,
        province
    );
  }

  // todo : Province Change
  provinceSelection.onchange = (e) => {
    citySelection.disabled = false;

    // todo : Clear all options from citySelection
    citySelection.length = 1;
    zipcodeSelection.length = 1;

    for (let city in provinceInfo[e.target.value]) {
    citySelection.options[citySelection.options.length] = new Option(
        city,
        city
    );
  }
  };

  // todo: city Changed
  citySelection.onchange = (e) => {
    zipcodeSelection.disabled = false;

    zipcodeSelection.length = 1;

    let zips =
        provinceInfo[provinceSelection.value][citySelection.value];

    for (let i = 0; i < zips.length; i++) {
      zipcodeSelection.options[zipcodeSelection.options.length] = new Option(
          zips[i],
          zips[i]
      );
    }
  };

}